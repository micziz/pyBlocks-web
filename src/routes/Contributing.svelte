<h1>Contribtuion guide</h1>

<p>Thanks for Contribtuing to pyBlocks! Your contribution is greatly appreciated. Before you open a PR (PULL REQUEST), please read these guidelines.</p>

<h2>I have a bug to report</h2>

<p>The only way we accept bug reports is by creating a new issue on <a href="https://github.com/micziz/pyblocks/issues">GitHub</a></p>

<p>You will find a template. Change it to your need, then submit a ISSUE.</p>

<h2>I want feature x implemented...</h2>

<p>Ask! Create a suggestion on the community tab, and if you are skilled engouph, you can even create a PR yourself.</p>

<h2>I have a bugfix/small documentation change. How do i contribute?</h2>

<p>1. Open a PR on <a href="https://github.com/micziz/pyblocks/pulls">github</a></p>
<p>2. Make sure you are on the correct branch. All pr's go to develop, and are then slowly merged into the master branch as time goes by.</p>
<p>3. Make a descriptive title/description. If we don't understand it, than how are we gonna merge it?</p>
<p>4. KISS Keep It Simple Stupid. Don't over engineer it.</p>
<p>5. Once you are ready, open the pr. Someone on the team will respond in a timeley manner, and will then merge your contributions.</p>