<h1>pyBlocks</h1>
  
<p>Build python scripts with 0 lines of code!</p>
  
<h2>Installation</h2>
  
<p>pyBlocks is pretty minimal. It requires no external libraries.</p>
  
<p>To install it, first follow the instructions for installing python <a href="https://www.python.org/downloads/">here</a>. You also need to install git <a href="https://git-scm.com">here</a></p>
  
<div id=installation>  
    <p>Clone the git repo</p>
    <code>git clone https://github.com/micziz/pyblocks</code>
      
    <p >Go to the directory</p>
    <code>cd pyblocks</code>
    
    <p>Run the file</p>
    <code>python3 main.py</code>
</div>
  
  
<div id=usage>
    <h2>Usage</h2>
  
    <code>python3 main.py</code>
</div>

